      // 游댳 M칠todo para cargar la tabla con la lista de personas
    private void cargarTabla() {
        if (listaPersonas == null || listaPersonas.isEmpty()) return;

        String[] columnas = {
            "Nombre", "Apellidos", "ID", "Estado Civil",
            "A침o Incorporaci칩n", "Dependencia", "Facultad", "Curso", "Labor"
        };

        Object[][] datos = new Object[listaPersonas.size()][columnas.length];

        for (int i = 0; i < listaPersonas.size(); i++) {
            Persona p = listaPersonas.get(i);
            datos[i][0] = p.getNombre();
            datos[i][1] = p.getApellidos();
            datos[i][2] = p.getNumeroIdentificacion();
            datos[i][3] = p.getEstadoCivil();

            // A침o de incorporaci칩n solo si es Empleado o subclase
            if (p instanceof Empleado) {
                datos[i][4] = ((Empleado) p).getA침oIncorporacion();
            } else {
                datos[i][4] = "";
            }

            // Rellenar solo la columna que aplica
            if (p instanceof Estudiante) datos[i][7] = ((Estudiante) p).getCurso();
            else if (p instanceof Profesor) datos[i][6] = ((Profesor) p).getFacultad();
            else if (p instanceof PersonalAdministrativo) datos[i][5] = ((PersonalAdministrativo) p).getDependencia();
            else if (p instanceof ServiciosVarios) datos[i][8] = ((ServiciosVarios) p).getLabor();
        }

        jTable1.setModel(new DefaultTableModel(datos, columnas));
    }

